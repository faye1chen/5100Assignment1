    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        //Fileter:image extensions
        FileNameExtensionFilter filter = new FileNameExtensionFilter("IMAGES","png","jpg","jpeg");
        fileChooser.addChoosableFileFilter(filter);
        
        int selected = fileChooser.showOpenDialog(null);
        if(selected == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            String selectedImagePath = file.getAbsolutePath();
            JOptionPane.showMessageDialog(null,selectedImagePath);
            //display image on jtable
            ImageIcon ImgIco= new ImageIcon(selectedImagePath);
            //resize image to fit jtable
            Image image = ImgIco.getImage().getScaledInstance(lblImage.getWidth(),lblImage.getHeight(),Image.SCALE_SMOOTH);
            lblImage.setIcon(new ImageIcon(image));
            
        }
        
    }//GEN-LAST:event_btnBrowseActionPerformed

    private void txtCellPhoneNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCellPhoneNumberKeyPressed
        // TODO add your handling code here:
        //10 digits
        String phonenumber =txtCellPhoneNumber.getText();
        int length = phonenumber.length();
        
        char c = evt.getKeyChar();
        
        //check for num 0 to 9
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            //check for length not more than 10 digit
            if(length<10){
                //editable true
                txtCellPhoneNumber.setEditable(true);
                
            }else{
                //not editable if length more than 10 digit
                txtCellPhoneNumber.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                // then allow editable
                txtCellPhoneNumber.setEditable(true);
            }else{
                txtCellPhoneNumber.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtCellPhoneNumberKeyPressed

    private void txtNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        if(Character.isLetter(c) ||Character.isWhitespace(c)||Character.isISOControl(c)){
            txtName.setEditable(true);
        }else{
            txtName.setEditable(true);
        }
    }//GEN-LAST:event_txtNameKeyPressed


