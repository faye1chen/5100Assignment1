    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().equals("")|| txtID.getText().equals("")||txtAge.getText().equals("")|| txtStartDate.getText().equals("")
                || txtLevel.getText().equals("") || txtTeamInfo.getText().equals("") || txtPositionTitle.getText().equals("")
                ||txtCellPhoneNumber.getText().equals("") ||txtEmailAddress.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Information must not be empty!");

        }else{
        String name = txtName.getText();
        String id = txtID.getText();
        int age = Integer.parseInt(txtAge.getText());
//        String gender = txtGender.getText();
        String gender = String.valueOf(comboGender.getSelectedItem());
        String startdate = txtStartDate.getText();
        String level = txtLevel.getText();
        String teaminfo = txtTeamInfo.getText();
        String positiontitle = txtPositionTitle.getText();
        int phonenumber = Integer.parseInt(txtCellPhoneNumber.getText());
        String emailaddress = txtEmailAddress.getText();
        ImageIcon ImgIco = (ImageIcon) lblImage.getIcon();

        
        EmployeeProfile ep = directory.addNewProfile();
        
        ep.setName(name);
        ep.setId(id);
        ep.setAge(age);
        ep.setGender(gender);
        ep.setStartdate(startdate);
        ep.setLevel(level);
        ep.setTeaminfo(teaminfo);
        ep.setPositiontitle(positiontitle);
        ep.setPhonenumber(phonenumber);
        ep.setEmailaddress(emailaddress);
        ep.setImgIco(ImgIco);
        
        

        
        JOptionPane.showMessageDialog(this,"New Employee Profile added.");
        
        txtName.setText("");
        txtID.setText("");
        txtAge.setText("");
//        txtGender.setText("");
        txtStartDate.setText("");
        txtLevel.setText("");
        txtTeamInfo.setText("");
        txtPositionTitle.setText("");
        txtCellPhoneNumber.setText("");
        txtEmailAddress.setText("");
        lblImage.setIcon(null);
        
    }//GEN-LAST:event_btnSaveActionPerformed
}
        
